// Get all unique categories
export const categories = [...new Set(Object.values(keywordMap))];

// Export keyword entries for training
export function getTrainingData(): Array<{ text: string; category: string }> {
  const trainingData: Array<{ text: string; category: string }> = [];
  
  for (const [keyword, category] of Object.entries(keywordMap)) {
    // Add the keyword itself
    trainingData.push({ text: keyword.toLowerCase(), category });
    
    // Add synthetic variations
    trainingData.push({ text: `payment to ${keyword.toLowerCase()}`, category });
    trainingData.push({ text: `${keyword.toLowerCase()} transaction`, category });
    trainingData.push({ text: `debited for ${keyword.toLowerCase()}`, category });
    trainingData.push({ text: `credited from ${keyword.toLowerCase()}`, category });
  }
  
  return trainingData;
}
